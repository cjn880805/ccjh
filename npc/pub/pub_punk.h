NPC_BEGIN(CNpub_punk);

virtual void create()
{
	set_name("小流氓", "punk" );

	set("icon", banditi);
	set("gender", "男性" );
	set("age", 18);
	set("long", "这是一个游手好闲的小流氓。");
	
	set_temp("apply/attack", 10);
	set_temp("apply/defense", 10);
	set("combat_exp", 500);
	set("shen_type", -1);
	set("str", 25);
	set("dex", 20);
	set("con", 18);
	set("int", 15);
	set("attitude","heroism");
	set("chat_chance", 2);
	carry_object("cloth")->wear();

	set_inquiry("王小二", ask_me);
	set_inquiry("翠花","翠花是个小寡妇，她家就在打谷场西边。");
	set_inquiry("鸡","鸡是王小二弄来的，味道还真不错。");

}

virtual char * chat_msg()
{
	switch (random(4) )
	{
	case 0:
		return  "流氓骂骂咧咧的说道：这真他妈的邪唬，老子手里一对天牌，王小二这杂种居然就摸出一对至尊来。";
	case 1:
		return  "流氓靠着墙角打起瞌睡来了，睡梦中也不知想到了什麽，嘴里哼哼道：我看见你哪雪白的大腿，我就起～了～坏～心～喽。";
	case 2: 
		return  "流氓摸了摸自己的口袋，叹了口气说道：唉，钱都让王小二骗去了，不能去找翠花了。";
	case 3:
		return  "流氓嘴角露出一丝邪笑，说道：翠花这娘儿们真够劲儿，真她奶奶的够～劲～儿!";
	}
	return "";
}

static char * ask_me(CNpc * who, CChar * me)
{
	if ( me->query_temp("marks/王") ) 
	{
		who->say("你还有完没完? 问话不要钱啊?", me);
	}
	else 
	{
		who->say("王小二啊，他不是本村的人，几个月前来才我们这儿。跟王小二赌钱总是输，我看他有捣鬼。这小子拳脚挺厉害的。",me);

		me->set_temp("marks/王", 1);
		who->say("你要找王小二的麻烦吧? 他就住在财神庙後面，他在那儿搭了个小棚子。", me);
	}

	who->SendMenu(me);
	return "";
}

NPC_END;